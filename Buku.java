    private void btnupdatebukuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdatebukuActionPerformed
        // TODO add your handling code here:
        String idBuku = txtidbuku.getText();
        String judul = txtjudulbuku.getText();
        String penerbit = txtpenerbitbuku.getText();
        try {
            final String sql = "Update buku set judul_buku= ?, penerbit =?, stok =? where id_buku= ?";
            final PreparedStatement pst = connection.prepareStatement(sql);
            pst.setString(1, judul);
            pst.setString(2, penerbit);
            pst.setInt(3, Integer.parseInt(txtstokbuku.getText()));
            pst.setString(4, idBuku);
            pst.executeUpdate();

            this.getAllData();
            this.refreshModel();
            this.id_auto();

            JOptionPane.showMessageDialog(this, "Berhasil Memperbarui");
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal Memperbarui");
        }
        txtjudulbuku.setText("");
        txtpenerbitbuku.setText("");
        txtstokbuku.setText("");
    }//GEN-LAST:event_btnupdatebukuActionPerformed

    private void btnhapusbukuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnhapusbukuActionPerformed
        // TODO add your handling code here:
        try {
            String id_buku = !txtidbuku.getText().trim().isEmpty() ? txtidbuku.getText().trim() : tblbuku.getValueAt(tblbuku.getSelectedRow(), 0).toString();
            String sql = "delete from buku where id_buku = ?";
            PreparedStatement ps = Connection.prepareStatement(sql);
            ps.setString(1, id_buku);
            ps.execute();
            this.getAllData();
            this.refreshModel();
            this.id_auto();

            JOptionPane.showMessageDialog(this, "Data behasil dihapus");
        } catch (SQLException | ArrayIndexOutOfBoundsException ex) {
            System.out.println(ex.getMessage());
        }

        txtjudulbuku.setText("");
        txtpenerbitbuku.setText("");
        txtstokbuku.setText("");
    }//GEN-LAST:event_btnhapusbukuActionPerformed

