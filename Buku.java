    public final void getAllData() {
        try {
            dataList.clear();
            String sql = "SELECT * FROM buku;";
            PreparedStatement ps = Connection.prepareStatement(sql);
            ps.execute();
            final ResultSet data = ps.getResultSet();
            while (data.next()) {
                final String[] row = new String[]{
                    data.getString("id_buku"),
                    data.getString("judul_buku"),                  
                    data.getString("penerbit"),
                    data.getString("stok")};
                    dataList.add(row);
            }
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
        JFrame frame = new JFrame();
        frame.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
        // frame.add(new JScrollPane(table));
        frame.pack();
        frame.setVisible(true);

    }

    public final void refreshModel() {
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(columns);
        for (String[] data : dataList) {
            model.addRow(data);
        }
        tblbuku.setModel(model);
    }
    
        public final void id_auto() {
        try {
            final Statement stat = connection.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_READ_ONLY);
            final String sql = "select max (right(id_buku, 1)) as no from buku";
            final ResultSet rs = stat.executeQuery(sql);
            while (rs.next()) {
                if (rs.first() == false) {
                    txtidbuku.setText("B0001");
                } else {
                    rs.last();
                    int set_id = rs.getInt(1) + 1;
                    String no = String.valueOf(set_id);
                    int id_next = no.length();
                    for (int a = 0; a < 4 - id_next; a++) {
                        no = "0" + no;
                    }
                    txtidbuku.setText("B" + no);
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(Buku.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
        
        public void cari() {
        DefaultTableModel tbl = new DefaultTableModel();
        tbl.addColumn("id_buku");
        tbl.addColumn("judul_buku");
        tbl.addColumn("penerbit");
        tbl.addColumn("stok");

        try {
            String sql = "Select*from buku where judul_buku like '%" + txtcaribuku.getText() + "%'";
            Statement st = connection.createStatement();
            ResultSet rs = st.executeQuery(sql);

            while (rs.next()) {
                tbl.addRow(new Object[]{
                    rs.getString("id_buku"),
                    rs.getString("judul_buku"),
                    rs.getString("penerbit"),
                    rs.getString("stok"),
                });
                tblbuku.setModel(tbl);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Anggota.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
   
