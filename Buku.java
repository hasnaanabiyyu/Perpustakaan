    private void btnsimpanbukuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanbukuActionPerformed
        // TODO add your handling code here:
        try {
            final String sql = "INSERT INTO buku(id_buku, judul_buku, penerbit, stok) VALUES (?,?,?,?)";
            final PreparedStatement ps = connection.prepareStatement(sql);
            if (txtidbuku.getText().isEmpty() || txtjudulbuku.getText().isEmpty() || txtpenerbitbuku.getText().isEmpty() || txtstokbuku.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Isi Semua Kolom terlebih dahulu");
            } else {
                ps.setString(1, txtidbuku.getText());
                ps.setString(2, txtjudulbuku.getText());
                ps.setString(3, txtpenerbitbuku.getText());
                ps.setInt(4, Integer.parseInt(txtstokbuku.getText()));
                ps.execute();
                JOptionPane.showMessageDialog(null, "Berhasil di simpan");
            }
            this.getAllData();
            this.refreshModel();
            this.id_auto();
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(null, "gagal disimpan");
            System.out.println(e.getMessage());
        }
        
        txtjudulbuku.setText("");
        txtpenerbitbuku.setText("");
        txtstokbuku.setText("");
    }//GEN-LAST:event_btnsimpanbukuActionPerformed

